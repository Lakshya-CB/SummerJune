package Lec_02;

public class loops_demo {
	public static void main(String[] args) {
		int kitne = 5; // intialization
		int chapat = 1;
		while (chapat <= kitne) {// condition
			System.out.println("lagao chapat");// repeated work
			chapat = chapat + 1;
		}
		System.out.println("end");
		System.out.println(chapat);
//		System.out.println("kuch bhi likhdo ");
	}
}
